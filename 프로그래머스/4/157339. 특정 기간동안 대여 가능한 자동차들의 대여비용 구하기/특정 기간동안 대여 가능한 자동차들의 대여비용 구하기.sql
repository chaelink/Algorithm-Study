SELECT A.CAR_ID, A.CAR_TYPE, ROUND((A.DAILY_FEE*30)*(100 - B.DISCOUNT_RATE)/100) AS FEE
FROM CAR_RENTAL_COMPANY_CAR A JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN B
ON A.CAR_TYPE = B.CAR_TYPE
WHERE B.DURATION_TYPE = '30일 이상'
AND A.CAR_TYPE IN('SUV', '세단')
AND A.CAR_ID NOT IN (
    SELECT CAR_ID FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY 
    WHERE START_DATE<= '2022-11-30' AND END_DATE>= '2022-11-01')
AND (A.DAILY_FEE*30)*(100 - B.DISCOUNT_RATE)/100 >= 500000 AND (A.DAILY_FEE*30)*(100 - B.DISCOUNT_RATE)/100 < 2000000
ORDER BY FEE DESC, A.CAR_TYPE, A.CAR_ID DESC;


# 자동차 종류는 세단, SUV
# 2022년 11월 1일부터 2022년 11월 30일까지 대여 가능
# 대여 금액이 50만원 이상 200만원 미만인 자동차